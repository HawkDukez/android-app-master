package com.lmmnb.guangzhouculturehelper.app.view.widget;

public class MyOption {
    private String op;
    private int Img;

    public MyOption(String op, int img)
    {
        this.op = op;
        this.Img = img;
    }

    public String getOp()
    {
        return op;
    }

    public int getImg()
    {
        return Img;
    }
}